function RegisterScreen(props) {



  const [firstName, setFirstName] = useState('');
  const [lastName, setLastName] = useState('');
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');

  const errors = {
    firstName: 'Aman',
    lastName: '',
    email: '',
    password: '',
  }
  console.log(errors.firstName);
  console.log(firstName);

  const [rePassword, setRePassword] = useState('');
  const userRegister = useSelector(state => state.userRegister);
  const { loading, userInfo, error } = userRegister;
  const dispatch = useDispatch();

  const redirect = props.location.search ? props.location.search.split("=")[1] : '/';
  useEffect(() => {
    if (userInfo) {
      props.history.push(redirect);
    }
    return () => {
      //
    };
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [userInfo]);

  const submitHandler = (e) => {
    e.preventDefault();
    if (rePassword === password)
      dispatch(register(firstName, lastName, email, password));
    else
      alert("password does not match");
  }
  return <div className="form">
    <form onSubmit={submitHandler} >
      <ul className="form-container">
        <li>
          <h2>Create Account</h2>
        </li>
        <li>
          {loading && <div><h1>Loading please wait...</h1> </div>}
          {error && <div>{error}</div>}
        </li>
        <li>
          <label htmlFor="firstName">
            FirstName
          </label>
          <input type="firstName" name="firstName" id="firstName" onChange={(e) => setFirstName(e.target.value)}>
          </input>
          <label htmlFor="lastName">
            LastName
          </label>
          <input type="lastName" name="lastName" id="lastName" onChange={(e) => setLastName(e.target.value)}>
          </input>
        </li>
        <li>
          <label htmlFor="email">
            Email
          </label>
          <input type="email" name="email" id="email" onChange={(e) => setEmail(e.target.value)}>
          </input>
        </li>
        <li>
          <label htmlFor="password">Password</label>
          <input type="password" id="password" name="password" onChange={(e) => setPassword(e.target.value)}>
          </input>
        </li>
        <li>
          <label htmlFor="rePassword">Re-Enter Password</label>
          <input type="password" id="rePassword" name="rePassword" onChange={(e) => setRePassword(e.target.value)}>
          </input>
        </li>
        <li>
          <button type="submit" className="button primary">Register</button>
        </li>
        <li>
          Already have an account?
          <Link to={redirect === "/" ? "signin" : "signin?redirect=" + redirect} className="button secondary text-center" >Create your amazona account</Link>

        </li>

      </ul>
    </form>
  </div>
}
export default RegisterScreen;